
public class Continhas {
	public static void main(String[] args) {
		int a, b, c;

		a = 25;
		b = 3;
		c = a + b;

		System.out.println("Valor de a = " + a);
		System.out.println("Valor de b = " + b);
		System.out.println("Valor de c = " + c);

		c = a - b;
		System.out.println("Valor de c = " + c);

		c = a * b;
		System.out.println("Valor de c = " + c);
		
		c = a / b;
		System.out.println("Valor de c = " + c);
		
		c = a % b;
		System.out.println("Valor de c = " + c);

	}

}
